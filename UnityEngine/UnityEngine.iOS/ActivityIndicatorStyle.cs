		///   <para>The standard white style of indicator (UIActivityIndicatorViewStyleWhite).</para>
		/// </summary>
		White,
		/// <summary>
		///   <para>The standard gray style of indicator (UIActivityIndicatorViewStyleGray).</para>
		/// </summary>
		Gray
	}
}
